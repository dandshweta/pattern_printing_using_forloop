// @description {print star pattern}
// Step1-Take empty variable let a="" to store star.
// step2-iterate the loop upto less than 5
// step3-print star Pattern.
function print() {
  let a = '';

  for (let i = 0; i < 5; i++) {
    a = a + '*';
    console.log(a);
  }
}
print();

